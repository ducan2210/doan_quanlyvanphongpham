    private void btn_themAnh1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_themAnh1MouseClicked
        // TODO add your handling code here:
        try {
            JFileChooser f = new JFileChooser(System.getProperty("user.dir") + "./Images/");
            f.setDialogTitle("Chọn Ảnh");
            f.showOpenDialog(null);
            File fileAnh = f.getSelectedFile();
            if (fileAnh != null) {
                if (fileAnh.getAbsolutePath().contains("./Images/")) {
                    srcAnh = fileAnh.getName();
                } else {
                    File dirAnh = new File("./Images/");
                    srcAnh = dirAnh.toURI().relativize(fileAnh.getCanonicalFile().toURI()).getPath();
                }
                // Nối đường dẫn tương đối đến thư mục Images/ vào phía trước tên file ảnh.
                srcAnh = "./Images/" + srcAnh;
                lb_hinh.setIcon(ResizeImage(srcAnh));
                chooseImg = true;
            }

        } catch (Exception e) {
            JOptionPane.showConfirmDialog(null, "Đã xảy ra sự cố! Không thể tải ảnh lên", "Thông báo!", JOptionPane.DEFAULT_OPTION);
            return;
        }
    }//GEN-LAST:event_btn_themAnh1MouseClicked

