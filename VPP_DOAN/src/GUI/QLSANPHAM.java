    private void btn_luuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_luuMouseClicked
        if(txt_tenSP.getText().isEmpty()||txt_mau.getText().isEmpty())
        {
            JOptionPane.showConfirmDialog(null, "Vui lòng nhập đầy đủ thông tin sản phẩm muốn thêm!!!", "Thông báo!", JOptionPane.DEFAULT_OPTION);
        }
        else{
            Object nccSelected = cbx_ncc1.getSelectedItem();
            String tenncc = nccSelected.toString();
            Object loaiSelected = cbx_loaiSP1.getSelectedItem();
            String tenloai = loaiSelected.toString();
            NhaCungCap ncc = NhaCungCapDAO.TimMaNCCTheoTenNCC(tenncc);
            String mancc = String.valueOf(ncc.getMaNCC());
            Pojo.SanPham sp = SanPhamDAO.TimMaloaiTheoTenLoai(tenloai);
            String maloai = String.valueOf(sp.getMaLoai());
            boolean kq = SanPhamDAO.kiemTraSanPham(txt_tenSP.getText(),  mancc, txt_mau.getText(), maloai);

            if(kq)
            {
                JOptionPane.showConfirmDialog(null, "Đã có mặt hàng này trong cửa hàng !!!", "Thông báo!", JOptionPane.DEFAULT_OPTION);
            }
            else{
                String src=srcAnh;
                if(SanPhamDAO.themSanPham(txt_tenSP.getText(), maloai, mancc, txt_mau.getText(),src))
                {
                    JOptionPane.showMessageDialog(this, "Thêm sản phẩm thành công");
                    hienThi();
                }
                else
                {
                    JOptionPane.showMessageDialog(this, "Thêm sản phẩm thất bại!!!");
                }
            }
            txt_tenSP.setEditable(false);
            txt_mau.setEditable(false);
            cbx_loaiSP1.setEnabled(false);
            cbx_ncc1.setEnabled(false);
        }
    }//GEN-LAST:event_btn_luuMouseClicked

